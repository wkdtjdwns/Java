package chap_02;

public class _05_Operator5 {
	public static void main(String[] args) {
		// 삼항 연산자
		// 결과 = (조건) ? (참일 경우 결과값) : (거짓일 경우 결과값)
		// 결과에는 (참일 경우의 결과값) 이나 (거짓일 경우 결과값)이 들어갈 수 있다
		int x = 3;
		int y = 5;
		int max = (x > y) ? x : y; // x가 y보다 크면 x값을, 아니라면 y값을 max변수에 대입한다.
		System.out.println(max);
		
		int min = (x < y) ? x : y; // x가 y보다 작으면 x값을, 아니라면 y값을 min변수에 대입한다.
		System.out.println(min);
		
		boolean b = (x == y) ? true : false; // x값과 y값이 같다면 true를, 아니라면 false를 b변수에 대입한다.
		System.out.println(b);
		
		String s = (x != y) ? "다르다" : "같다"; // x값과 y값이 다르면 다르다를, 아니라면 같다를 s변수에 대입한다.
		System.out.println(s);
		
		int num = -10;
		// 삼항연산자를 num 값이 음수 양수 상관없이 절대값을 계산해서 absNum에 넣으시오.
		int absNum = (num < 0) ? -num : num;
		System.out.println(absNum);
	}
}
