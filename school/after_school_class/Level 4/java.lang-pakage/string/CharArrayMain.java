package lang.string;

public class CharArrayMain {

    public static void main(String[] args) {
        // char를 이용하여 hello 출력
        // String을 이용하여 hello 출력
        char[] charHello = { 'h', 'e', 'l', 'l', 'o' };
        String stringHello = "hello";

        System.out.println(charHello);
        System.out.println(stringHello);
    }
}
