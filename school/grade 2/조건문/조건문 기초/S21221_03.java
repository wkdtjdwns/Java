package S21221_00;

public class S21221_03 {

	public static void main(String[] args) {
		 int input = 0;
	        
		 if (input == 0) {
			 System.out.println("a : 값은 0 입니다");
		 }
	        
		 if (input != 0) {
			 System.out.println("a : 값은 0이 아닙니다");
		 }
	        
		 input = 3;
	        
		 if (input == 0) {
			 System.out.println("b : 값은 0 입니다");
		 } else {
			 System.out.println("b : 값은 0이 아닙니다");
		 }
	}
}

/*
실행 결과
--------------------
a : 값은 0 입니다
b : 값은 0이 아닙니다
*/
